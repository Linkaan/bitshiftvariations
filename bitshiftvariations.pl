sub g {
    return +(3 & $_[1] & $_[0] * (ord(substr 3 & $_[0] >> 16 ? 'BY}6YB6%' : 'Qj}6jQ6%', $_[2] % 8, 1) + 51) >> $_[3]) << 4;
}
for ($i = 0;; ++$i) {
    print pack('C', g($i, 1, $n = $i >> 14, 12) + g($i, $s = $i >> 17, $n ^ $i >> 13, 10) + g($i, $s / 3, $n + ($i >> 11) % 3, 10) + g($i, $s / 5, 8 + $n - ($i >> 10) % 3, 9));
}
